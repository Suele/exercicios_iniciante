// Algoritmo que verifica se o aluno foi aprovado ou reprovado.

package iniciante;

public class Nota {
	public static void main(String[] args) {

		int nota;
		nota = 50;

		if (nota >= 60) {
			System.out.println("Aluno Aprovado.");
		} else {
			System.out.println("Aluno Reprovado.");
		}
	}

}
