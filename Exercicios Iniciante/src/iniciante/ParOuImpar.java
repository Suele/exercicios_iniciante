//Algoritmo que verifica se determinado valor é par ou Ímpar.

package iniciante;

public class ParOuImpar {
	public static void main(String[] args) {

		int numero = 9;

		if (numero % 2 == 0) {
			System.out.println(numero + " é" + " par.");
		} else {
			System.out.println(numero + " é" + " ímpar.");
		}

	}
}
