// Algoritmo que verifica se um número é positivo ou negativo.

package iniciante;

public class PositivoOuNegativo {

	public static void main(String[] args) {
		
		int numero = -24;
		
		if (numero > 0) {
			System.out.println(numero + " é positivo.");
		}else {
			System.out.println(numero + " é negativo.");
		}

	}

}
